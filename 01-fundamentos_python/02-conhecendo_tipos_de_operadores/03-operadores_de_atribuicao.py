print("====== Atribuição simples ======")
saldo = 500

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com adição ======")
saldo = 500
saldo += 200    # saldo = saldo + 200

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com subtração ======")
saldo = 500
saldo -= 100    # saldo = saldo - 100

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com multiplicação ======")
saldo = 500
saldo *= 2   # saldo = saldo * 2

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com divisão ======")
saldo = 500
saldo /= 5   # saldo = saldo / 5

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com divisão inteira ======")
saldo = 500
saldo //= 5   # saldo = saldo // 5

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com módulo ======")
saldo = 500
saldo %= 480   # saldo = saldo %= 480 

print(f"Saldo: {saldo}\n")

# =========================================================================
print("====== Atribuição com exponenciação ======")
saldo = 80
saldo **= 2   # saldo = saldo **= 2

print(f"Saldo: {saldo}\n")
